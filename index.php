<p>
Akhtarcarpet adalah sebuah merkdagang yang berasal dari singapore. Akhtarcarpet sendiri bergerak dalam bidang penjualan karpet serta servis karpet.
Perusahaan ini berdiri sejak tahun 2016 kemudian terus berkembang dan memiliki 200 cabang. 
<p>
Akhtarcarpet sendiri saat ini sudah memiliki gerai di beberapa negara asia tenggara yaitu Singapore, Indonesia, Malaysia dan Brunai.
